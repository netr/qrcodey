def test_calculate_finder_pattern_positions():
    qr = QrCode(1)
    assert qr.calculate_top_right() == [14, 0]
    assert qr.calculate_bottom_left() == [0, 14]

    qr = QrCode(32)
    assert qr.calculate_top_right() == [138, 0]
    assert qr.calculate_bottom_left() == [0, 138]


